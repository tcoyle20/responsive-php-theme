
<h1>Lightweight, Responsive PHP Theme for Web Developers</h1>
<h2>Sub head</h2>
<h3>Third head</h3>
<p>Built with PHP, Foundation 4, Sass+Compass.</p>
<p>I made this system to help me quickly knock off small projects where a CMS or framework 
	would be too much. It seems that everyone is in a rush to install WordPress, etc.</p>
<p>Essentially this a blank canvas for web developers and designers, it has a lot of
	time-saving goodness already organized inside.</p>
<p>The PHP logic is separated from the design layer and there are built in
	Database Classes and a configuration file.</p>
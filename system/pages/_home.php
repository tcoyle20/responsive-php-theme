
<h1>A Mini MVC PHP Framework</h1>
<p>Responsive web templates built with Compass, Zurb Foundation 4 and PHP classes.</p>
<p>I made this system to help me quickly knock off small projects where a CMS or framework 
	would be too much. It seems that everyone is in a rush to install WordPress, etc.</p>
<p>I've always thought that small-to-medium size sites can (and should) still be dynamic, feature-rich 
	and easy to maintain - but without the bloat and plugins!</p>
